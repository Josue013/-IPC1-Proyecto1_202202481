    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String codigo = CodigoInput.getText();
        String contrasenia = PasswordInput.getText();

        if (codigo.equals("admin") && contrasenia.equals("admin")) {
            this.setVisible(false);
            AdministradorProfesores ap = new AdministradorProfesores();
            ap.setVisible(true);
        } else if (codigo.equals(CrearProfesor.Codigo) && contrasenia.equals(CrearProfesor.Contrasenia)){
            this.setVisible(false);
            InicioProfesores ap = new InicioProfesores();
            ap.setVisible(true);
        }else{
            this.setVisible(true);
            JOptionPane.showMessageDialog(null, "Datos incorrectos, intenta de nuevo");
            
        }

    }//GEN-LAST:event_jButton1ActionPerformed

